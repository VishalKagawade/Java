package stringmethods;

public class CharAt {
	
	// String Method --> charAt(int index);
	
	public static void main(String[] args) {
		
		String s = "automation";
		
		char var = s.charAt(4);
		
		System.out.println(var);
	}
	
	// if char index is not in size limit then it will give the following exception StringIndexOutOfBondException

}
