package array;

import java.util.Arrays;

public class PrintingArrayUsingForEach {
	
	public static void main(String[] args) {
		
	String [] a = {"av","dk","vk","sk"};
	
	for(String b:a)
	
	System.out.println(b);
	System.out.println(" ");
	
	int [] c = {1,2,5,3,8,6,7};
	
	for(int v:c)
		System.out.println(v);
	
	}
	

}
